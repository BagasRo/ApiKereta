    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed


         String ticketid = txtticketno.getText();
         String nokereta = txtnokereta.getText();     
         String namakereta = txtkereta.getText();   
         String kelaskereta = txtkelaskereta.getText();  
         String atasnama = txtnama.getText();
         String asalkereta = txtasaltiket.getText();
         String tujuankereta = txttujuantiket.getText();
         String harga = txtharga.getText();
         String nomornik = txtnik.getText();
         String kursi = txtgerbong.getSelectedItem().toString().trim()+"/" + txtbangku.getSelectedItem().toString().trim();
         String berangkat = txtberangkat.getText();
         
         DateFormat da = new SimpleDateFormat("yyyy-MM-dd");
         String date = da.format(txtdate.getDate());
        

         
         
         
         
        try {
    Class.forName("com.mysql.jdbc.Driver");
    con = DriverManager.getConnection("jdbc:mysql://localhost/apikereta", "root", "");
    
    // Cek apakah kombinasi data sudah ada
    String checkQuery = "SELECT COUNT(*) FROM tiket WHERE kereta = ? AND kelaskereta = ? AND kursi = ? AND tanggal = ? AND asal = ? AND tujuan = ?";
    PreparedStatement checkStatement = con.prepareStatement(checkQuery);
    checkStatement.setString(1, namakereta);
    checkStatement.setString(2, kelaskereta);
    checkStatement.setString(3, kursi);
    checkStatement.setString(4, date);
    checkStatement.setString(5, asalkereta);
    checkStatement.setString(6, tujuankereta);
    
    ResultSet resultSet = checkStatement.executeQuery();
    resultSet.next();
    int count = resultSet.getInt(1);
    
    if (count > 0) {
        JOptionPane.showMessageDialog(null, "Kursi Sudah Ditempati");
    } else {
        // Jika kombinasi data belum ada, lakukan operasi penambahan data ke database
        pst = con.prepareStatement("INSERT INTO tiket (id, nama, nik, nokereta, kereta, kelaskereta, kursi, asal, tujuan, harga, tanggal, berangkat) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)");
        
        pst.setString(1, ticketid);
        pst.setString(2, atasnama);
        pst.setString(3, nomornik);
        pst.setString(4, nokereta);
        pst.setString(5, namakereta);
        pst.setString(6, kelaskereta);
        pst.setString(7, kursi);
        pst.setString(8, asalkereta);
        pst.setString(9, tujuankereta);
        pst.setString(10, harga);
        pst.setString(11, date);
        pst.setString(12, berangkat);
        
        pst.executeUpdate();
        
        JOptionPane.showMessageDialog(null, "Tiket Terpesan.........");
    }
    autoID();
    
} catch (ClassNotFoundException ex) {
    Logger.getLogger(PesanTiket.class.getName()).log(Level.SEVERE, null, ex);
} catch (SQLException ex) {
    Logger.getLogger(PesanTiket.class.getName()).log(Level.SEVERE, null, ex);
}

        
    }//GEN-LAST:event_jButton2ActionPerformed

    
    
    
    
    
