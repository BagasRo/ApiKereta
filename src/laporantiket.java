    int totalIncome = 0;
    
    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    int selectedIndex = jTable1.getSelectedRow();
    
    String text = model.getValueAt(selectedIndex, 0).toString(); 
    jTextArea1.append(text + "\n");

    DecimalFormat decimalFormat = new DecimalFormat("#,##0");
    decimalFormat.setParseBigDecimal(true);
    
    String hargaFormatted = (String) model.getValueAt(selectedIndex, 9); 
    hargaFormatted = hargaFormatted.replace("Rp. ", "").replace(".", ""); 

    try {
        Number hargaNumber = decimalFormat.parse(hargaFormatted);
        int harga = hargaNumber.intValue();
        totalIncome += harga;
    } catch (ParseException e) {
        e.printStackTrace();
    }

    String formattedIncome = "Rp. " + NumberFormat.getNumberInstance(Locale.ITALY).format(totalIncome);
    jLabel2.setText(formattedIncome);
        
    }//GEN-LAST:event_jTable1MouseClicked

    
    
    
    
