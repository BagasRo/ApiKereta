    public void LoadData()
    {
      
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/apikereta","root","");
            pst = con.prepareStatement("SELECT * from tiket");
             ResultSet rs = pst.executeQuery();
             
             ResultSetMetaData rsm = rs.getMetaData();
             int c;
             c = rsm.getColumnCount();
             
             DefaultTableModel Df = (DefaultTableModel)jTable1.getModel();
             Df.setRowCount(0);
             
             while(rs.next())
             {
                 Vector v2 = new Vector();
                 
                 for(int i = 1; i<= c; i ++)
                 {
                     v2.add(rs.getString("id"));
                  v2.add(rs.getString("nama"));
                  v2.add(rs.getString("nik"));
                  v2.add(rs.getString("nokereta"));
                  v2.add(rs.getString("kereta"));
                  v2.add(rs.getString("kelaskereta"));
                  v2.add(rs.getString("kursi"));
                  v2.add(rs.getString("asal"));
                  v2.add(rs.getString("tujuan"));       
                  
                  
                  int harga = rs.getInt("harga");
                    DecimalFormat decimalFormat = new DecimalFormat("#,##0");
                    String hargaFormatted = "Rp. " + decimalFormat.format(harga);
                    v2.add(hargaFormatted);
                  v2.add(rs.getString("tanggal"));
                  v2.add(rs.getString("berangkat"));

                 }
                 
                 Df.addRow(v2);
           
             }          
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(laporantiket.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(laporantiket.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }
    
